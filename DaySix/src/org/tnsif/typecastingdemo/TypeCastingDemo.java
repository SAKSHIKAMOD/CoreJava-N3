package org.tnsif.typecastingdemo;
// Program o demonstrate on type casting
public class TypeCastingDemo {

	public static void main(String[] args) {
		// Implicit type casting 
		int x=15;
		float y= x;
		System.out.println("The Output value of y is :"+y);
		
		// Explicit type casting
		double d=12.5;
		long l=(long )d;
		System.out.println("The Output value of l is :"+l);
		
	}

}
